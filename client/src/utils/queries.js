import { gql } from '@apollo/client';

export const QUERY_TICKETS = gql`
  query getTickets($category: ID) {
    tickets(category: $category) {
      _id
      name
      description
      price
      quantity
      image
      category {
        _id
      }
    }
  }
`;

export const QUERY_CHECKOUT = gql`
  query getCheckout($tickets: [ID]!) {
    checkout(products: $tickets) {
      session
    }
  }
`;

export const QUERY_ALL_PRODUCTS = gql`
  {
    tickets {
      _id
      name
      description
      price
      quantity
      category {
        name
      }
    }
  }
`;

export const QUERY_CATEGORIES = gql`
  {
    categories {
      _id
      name
    }
  }
`;

export const QUERY_USER = gql`
  {
    user {
      firstName
      lastName
      orders {
        _id
        purchaseDate
        products {
          _id
          name
          description
          price
          quantity
          image
        }
      }
    }
  }
`;
